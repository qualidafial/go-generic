package maps

// Keys returns a slice of the keys in a map. The order of keys in the slice is undefined.
func Keys[K comparable, V any](m map[K]V) []K {
	ks := make([]K, 0, len(m))
	for k := range m {
		ks = append(ks, k)
	}
	return ks
}

// Values returns a slice of the values in a map. The order of values in the slice is undefined.
func Values[K comparable, V any](m map[K]V) []V {
	vs := make([]V, 0, len(m))
	for _, v := range m {
		vs = append(vs, v)
	}
	return vs
}
